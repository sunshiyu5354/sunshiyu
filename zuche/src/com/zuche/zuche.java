package com.zuche;

public class zuche {  
    int number;  
    String brand;  
    double fee;  
    int personCapacity;  
    double goodCapacity;  
      
    public  zuche(int number, String brand, double fee){   //构造方法  
        this.number = number;  
        this.brand = brand;  
        this.fee = fee;  
    }  
      
    public int getNumber(){  
        return number;  
    }  
      
    public String getBrand(){  
        return brand;  
    }  
      
    public double getFee(){  
        return fee;  
    }  
      
    public int getPersonCapacity(){  
        return personCapacity;  
    }  
      
    public double getGoodCapacity(){  
        return goodCapacity;  
    }  
      
}  


